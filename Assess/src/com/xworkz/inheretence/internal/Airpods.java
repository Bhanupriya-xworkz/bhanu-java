package com.xworkz.inheretence.internal;


public class AirPods {

    public AirPods() {
        System.out.println("No-args constructor of AirPods");
    }

    public void playMusic() {
        System.out.println("Playing music through the AirPods...");
    }

    public void pauseMusic() {
        System.out.println("Pausing music on the AirPods...");
    }

    public void adjustVolume() {
        System.out.println("Adjusting volume on the AirPods...");
    }

    public void connectToDevice() {
        System.out.println("Connecting AirPods to a device...");
    }

    public void disconnectFromDevice() {
        System.out.println("Disconnecting AirPods from the device...");
    }
}