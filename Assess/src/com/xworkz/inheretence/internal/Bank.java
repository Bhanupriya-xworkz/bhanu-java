package com.xworkz.inheretence.internal;

public class Bank {

        // Constructor for Bank
        public Bank() {
            System.out.println("No-args constructor of Bank");
        }

        public void openAccount() {
            System.out.println("Opening a new account at the bank...");
        }

        public void depositMoney() {
            System.out.println("Depositing money into the bank account...");
        }

        public void withdrawMoney() {
            System.out.println("Withdrawing money from the bank account...");
        }

        public void checkBalance() {
            System.out.println("Checking the bank account balance...");
        }

        public void closeAccount() {
            System.out.println("Closing the bank account...");
        }
    }

