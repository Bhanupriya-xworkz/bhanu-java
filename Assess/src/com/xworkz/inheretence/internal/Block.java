package com.xworkz.inheretence.internal;

public class Block {

        public Block() {
            System.out.println("no-args constructor of Block");
        }

        public void breakBlock() {
            System.out.println("Breaking the block");
        }

        public void stackBlock() {
            System.out.println("Stacking the block");
        }

        public void arrangeBlock() {
            System.out.println("Arranging the block");
        }

        public void paintBlock() {
            System.out.println("Painting the block");
        }

        public void cleanBlock() {
            System.out.println("Cleaning the block");
        }
    }

