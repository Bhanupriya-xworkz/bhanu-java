package com.xworkz.inheretence.internal;

public class Bottle {

        public Bottle() {
            System.out.println("No-args constructor of Bottle");
        }

        public void fillWater() {
            System.out.println("Filling the bottle with water...");
        }

        public void drinkWater() {
            System.out.println("Drinking water from the bottle...");
        }

        public void cleanBottle() {
            System.out.println("Cleaning the bottle...");
        }

        public void checkCapacity() {
            System.out.println("Checking the capacity of the bottle...");
        }

        public void closeBottle() {
            System.out.println("Closing the bottle...");
        }
    }

