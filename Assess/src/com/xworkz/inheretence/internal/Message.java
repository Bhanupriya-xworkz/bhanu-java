package com.xworkz.inheretence.internal;

public class Message {

        // Constructor for Message
        public Message() {
            System.out.println("No-args constructor of Message");
        }

        public void composeMessage() {
            System.out.println("Composing a traditional message...");
        }

        public void sendMessage() {
            System.out.println("Sending a traditional message...");
        }

        public void receiveMessage() {
            System.out.println("Receiving a traditional message...");
        }

        public void deleteMessage() {
            System.out.println("Deleting the traditional message...");
        }

        public void displayMessage() {
            System.out.println("Displaying the traditional message...");
        }
    }
