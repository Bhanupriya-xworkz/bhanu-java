package com.xworkz.inheretence.internal;

public class Rubber {

        public Rubber() {
            System.out.println("No-args constructor of Rubber");
        }

        public void stretch() {
            System.out.println("Stretching the rubber...");
        }

        public void bounce() {
            System.out.println("Bouncing the rubber...");
        }

        public void mold() {
            System.out.println("Molding the rubber...");
        }

        public void roll() {
            System.out.println("Rolling the rubber...");
        }

        public void tear() {
            System.out.println("Tearing the rubber...");
        }
    }
