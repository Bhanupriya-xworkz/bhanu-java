package com.xworkz.inheretence.internal;

public class Window {
        public void W()
        {
            System.out.println("no-args const of window");
        }

        public void open()
        {
            System.out.println("window is being opened");
        }

        public void close()
        {
            System.out.println("window is being closed");
        }

        public void lock()
        {
            System.out.println("window is being locked");
        }

        public void unlock()
        {
            System.out.println("window is being unlocked");
        }
    }

