package com.xworkz.method.internal;

    public class Apple {

        public Apple() {
            System.out.println("No-args constructor of Apple");
        }

        public void grow() {
            System.out.println("Apple is growing on the tree...");
        }

        public void ripen() {
            System.out.println("Apple is ripening...");
        }

        public void pluck() {
            System.out.println("Apple is being plucked...");
        }

        public void eat() {
            System.out.println("Apple is being eaten...");
        }

        public void juice() {
            System.out.println("Apple is being turned into juice...");
        }

        public void store() {
            System.out.println("Apple is being stored...");
        }
    }


