package com.xworkz.method.internal;

    public class Banana {

        public Banana() {
            System.out.println("No-args constructor of Banana");
        }

        public void peel() {
            System.out.println("Peeling the banana...");
        }

        public void eat() {
            System.out.println("Eating the banana...");
        }

        public void store() {
            System.out.println("Storing the banana in a cool place...");
        }

        public void ripen() {
            System.out.println("The banana is ripening...");
        }

        public void throwAway() {
            System.out.println("Throwing away the banana peel...");
        }

        public void taste() {
            System.out.println("Tasting the sweet banana...");
        }
    }

