package com.xworkz.method.internal;

    public class Cat {

        public Cat() {
            System.out.println("No-args constructor of Cat");
        }

        public void meow() {
            System.out.println("Cat is meowing...");
        }

        public void sleep() {
            System.out.println("Cat is sleeping...");
        }

        public void eat() {
            System.out.println("Cat is eating...");
        }

        public void play() {
            System.out.println("Cat is playing...");
        }

        public void groom() {
            System.out.println("Cat is grooming itself...");
        }

        public void reproduce() {
            System.out.println("Cat is reproducing...");
        }
    }


