package com.xworkz.method.internal;

    public class Chair {

        public Chair() {
            System.out.println("No-args constructor of Chair");
        }

        public void sit() {
            System.out.println("Sitting on the chair...");
        }

        public void move() {
            System.out.println("Chair is being moved...");
        }

        public void breakChair() {
            System.out.println("Chair is broken...");
        }

        public void clean() {
            System.out.println("Cleaning the chair...");
        }

        public void adjust() {
            System.out.println("Adjusting the chair...");
        }

        public void decorate() {
            System.out.println("Decorating the chair...");
        }
    }
