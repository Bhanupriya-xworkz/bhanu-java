package com.xworkz.method.internal;

    public class Globe {

        public Globe() {
            System.out.println("No-args constructor of Globe");
        }

        public void rotate() {
            System.out.println("Globe is rotating...");
        }

        public void display() {
            System.out.println("Globe is displaying the world map...");
        }

        public void hold() {
            System.out.println("Globe is being held...");
        }

        public void clean() {
            System.out.println("Cleaning the globe...");
        }

        public void store() {
            System.out.println("Storing the globe...");
        }

        public void lightUp() {
            System.out.println("Globe is lighting up...");
        }

    }
