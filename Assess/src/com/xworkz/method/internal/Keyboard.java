package com.xworkz.method.internal;

    public class Keyboard {

        public Keyboard() {
            System.out.println("No-args constructor of Keyboard");
        }

        public void type() {
            System.out.println("Typing on the keyboard...");
        }

        public void connect() {
            System.out.println("Keyboard is connected...");
        }

        public void clean() {
            System.out.println("Cleaning the keyboard...");
        }

        public void disconnect() {
            System.out.println("Keyboard is disconnected...");
        }

        public void changeBatteries() {
            System.out.println("Changing batteries of the keyboard...");
        }

        public void store() {
            System.out.println("Storing the keyboard...");
        }
    }
