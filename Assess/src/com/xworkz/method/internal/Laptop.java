package com.xworkz.method.internal;

public class Laptop {

        public Laptop() {
            System.out.println("No-args constructor of Laptop");
        }

        public void bootUp() {
            System.out.println("Laptop is booting up...");
        }

        public void browse() {
            System.out.println("Laptop is browsing the web...");
        }

        public void playGames() {
            System.out.println("Laptop is playing games...");
        }

        public void watchMovies() {
            System.out.println("Laptop is watching movies...");
        }

        public void shutDown() {
            System.out.println("Laptop is shutting down...");
        }

        public void charge() {
            System.out.println("Laptop is charging...");
        }
    }

