package com.xworkz.method.internal;

public class Ring {
        public Ring() {
            System.out.println("No-args constructor of Ring");
        }

        public void wear() {
            System.out.println("Ring is being worn...");
        }

        public void sparkle() {
            System.out.println("Ring is sparkling...");
        }

        public void adjust() {
            System.out.println("Ring is being adjusted...");
        }

        public void remove() {
            System.out.println("Ring is being removed...");
        }

        public void clean() {
            System.out.println("Ring is being cleaned...");
        }

        public void store() {
            System.out.println("Ring is being stored safely...");
        }
    }
