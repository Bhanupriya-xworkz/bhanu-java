package com.xworkz.method.internal;

public class Robot {

    public Robot() {
        System.out.println("No-args constructor of cockroach");
    }

    public void crawl() {
        System.out.println("Cockroach is crawling...");
    }

    public void fly() {

        System.out.println("Cockroach is flying...");
    }

    public void hide() {

        System.out.println("Cockroach is hiding...");
    }

    public void eat() {

        System.out.println("Cockroach is eating...");
    }

    public void shedSkin() {

        System.out.println("Cockroach is shedding its skin...");
    }

    public void reproduce() {

        System.out.println("Cockroach is reproducing...");
    }
}

