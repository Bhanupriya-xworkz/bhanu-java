package com.xworkz.method.internal;

    public class Slipper {

        public Slipper() {
            System.out.println("No-args constructor of Slipper");
        }

        public void wear() {
            System.out.println("Slipper is being worn...");
        }

        public void remove() {
            System.out.println("Slipper is being removed...");
        }

        public void clean() {
            System.out.println("Slipper is being cleaned...");
        }

        public void store() {
            System.out.println("Slipper is being stored...");
        }

        public void repair() {
            System.out.println("Slipper is being repaired...");
        }

        public void slip() {
            System.out.println("Slipper is slipping...");
        }
    }

