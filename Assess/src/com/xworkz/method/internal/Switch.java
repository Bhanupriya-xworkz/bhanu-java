package com.xworkz.method.internal;

    public class Switch {

        public Switch() {
            System.out.println("Switch is initialized.");
        }

        public void turnOn() {
            System.out.println("Switch is turned ON...");
        }

        public void turnOff() {
            System.out.println("Switch is turned OFF...");
        }

        public void displayStatus() {
            System.out.println("Switch is in its default OFF state.");
        }
    }

